                    <h4>Столешницы</h4>
                    <p style="margin-bottom: 20px;">Столешницы из натурального дерева обладают высоким эстетическим качеством и приятными тактильными ощущениями. Дерево – это теплый, натуральный и экологически чистый материал, отлично сочетающийся с металлом, стеклом.
                    Для изготовления деревянных столешниц подходит исключительно древесина твердых пород (дуб, ясень и т.д.). Процесс производства столешниц из массива дерева требует соответствующего оборудования и опыта, от совокупности этих качеств зависит долговечность материала. Требования к лицевой стороне достаточно жесткие: не допускаются сучки и дефекты материала, покрытие выполняется износостойкими лаками или маслами глубокой пропитки. При изготовлении кухонных столешниц, так же используют влагостойкий клей и покрытие дополнительным слоем лака. Такая технология позволяет получить прочную и износостойкую поверхность. Края столешницы тщательно фрезеруют и обрабатывают. К качеству материала и шлифовки тыльной стороны требования минимальные, однако, влагостойкость покрытия должна быть на высоком уровне.</p>
