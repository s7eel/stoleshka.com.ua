                                <h4>Прямые фасады</h4>
                    <p style="margin-bottom: 20px;">Прямые фасады отлично подойдут для мебели и кухонных гарнитуров. В отличии от филенчатых, прямые фасады значительно дешевле, т.к. изготавливаются одной деталью из мебельного щита, толщиною 16-22мм. Требуют тщательной подборки по текстуре, как в детали, так и в композиции в целом, а так же учета направления волокон в ламелях, что бы компенсировать склонность материала к изгибу.</p>
