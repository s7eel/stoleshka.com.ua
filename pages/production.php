                    <h4>Предоставляем качественные услуги</h4>

                    <ul>
                        <li><b>1.	Подбираем породу дерева: </b>ясень, дуб.
                            Для производства столешниц, ступенек, подоконников мы используем только твердые породы дерева, что обеспечить износостойкость изделия. Дуб имеет красивую равномерную темную текстуру и высокую стойкость к гниению. Ясень имеет контрастную (сочетание белого и коричневого цветов) текстуру.</li>
                        <li><b>2.	Склеиваем: </b>цельноламельный (склейка только по ширине), сращенный (склейка по длине и по ширине).
                            Цельноламельный мебельный щит  при правильной подборке ламелей по цвету и текстуре имитирует массив, выглядит богато и подчеркивает натуральность материала. Сращенный щит напротив выглядит более пестрым и не концентрирует внимание на текстуре дерева, при этом обладает большей прочностью и стойкостью к кручению.</li>
                        <li><b>3.	Применяем клей: </b>влагостойкий, невлагостойкий.
                            Влагостойкие клея применятся для помещений с повышенным уровнем влажности, или для улицы.</li>
                        <li><b>4.	Обрабатываем:</b> с обработанными торцами, без обработанных торцов.
                            Наше предприятие может предложить клиентам, как заготовку из мебельного щита с припуском 6-15 мм, так и отформатированную деталь с шлифовкой торцов и фасками необходимого радиуса. Так же выполняем криволинейный раскрой.</li>
                        <li><b>5.	Покрываем:</b> без покраски, лак, лак + тон.
                            Не менее важным, чем качество материала, является защитное покрытие изделия. Дерево достаточно гигроскопичный материал, и ему свойственно набухание и усыхание. С некачественным покрытием данные процессы неизбежны, что приводит к изменению размеров, кручению и в итоге разрушению изделия.</li>
                        <li><b>6.   Изготавлеваем детали сложной формы:</b>  изготавливаем детали с криволинейными поверхностями по чертежу или шаблону. Клеем щиты с разной толщиной элементов.
                            </li>
